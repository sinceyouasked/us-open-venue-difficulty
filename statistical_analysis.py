import numpy as np
from scipy import stats

# ==============================================================================
# SECTION 1: PRIMARY DATASET
# Modern era U.S. Open FSA+/- (2010-2025, Data Golf verified)
# ==============================================================================

# Full dataset: (year, venue, fsa_plus_minus, winner, win_vs_par, tier)
MODERN_DATA = [
    (2013, "Merion (East)",            4.55, "Justin Rose",          +1, "T2"),
    (2018, "Shinnecock Hills",         4.39, "Brooks Koepka",        +1, "T3"),  # Tier 3: condition-sensitive (partial setup inflation)
    (2020, "Winged Foot (West)",       4.07, "Bryson DeChambeau",    -6, "T1"),
    (2010, "Pebble Beach",             3.98, "Graeme McDowell",      -1, "T3"),
    (2025, "Oakmont CC",               3.82, "J.J. Spaun",           -1, "T1"),
    (2012, "Olympic Club (Lake)",      3.47, "Webb Simpson",         +1, "T2"),
    (2015, "Chambers Bay",             3.45, "Jordan Spieth",        -5, "T3"),
    (2016, "Oakmont CC",               3.33, "Dustin Johnson",       -4, "T1"),
    (2014, "Pinehurst No. 2",          3.09, "Martin Kaymer",        -9, "T3"),
    (2024, "Pinehurst No. 2",          2.78, "Bryson DeChambeau",    -6, "T3"),
    (2022, "The Country Club",         2.60, "Matt Fitzpatrick",     -6, "T3"),
    (2021, "Torrey Pines (South)",     2.27, "Jon Rahm",             -6, "T3"),
    (2023, "LACC (North)",             1.76, "Wyndham Clark",        -10, "T4"),
    (2011, "Congressional (Blue)",     1.64, "Rory McIlroy",         -16, "T4"),
    (2019, "Pebble Beach",             1.22, "Gary Woodland",        -13, "T4"),
    (2017, "Erin Hills",               1.14, "Brooks Koepka",        -16, "T4"),
]

fsa_values = np.array([d[2] for d in MODERN_DATA])
tiers      = [d[5] for d in MODERN_DATA]

# Tier groupings
tier1   = np.array([d[2] for d in MODERN_DATA if d[5] == "T1"])  # n=3
tier2   = np.array([d[2] for d in MODERN_DATA if d[5] == "T2"])  # n=2
tier3   = np.array([d[2] for d in MODERN_DATA if d[5] == "T3"])  # n=5 (excl. Pebble 2019)
tier4   = np.array([d[2] for d in MODERN_DATA if d[5] == "T4"])  # n=3

# NOTE: Tier 3 includes Shinnecock 2018 (partial setup inflation caveat noted
# in manuscript §3.1), Pebble Beach 2010, Chambers Bay 2015, Pinehurst 2014,
# Pinehurst 2024, The Country Club 2022, Torrey Pines 2021.
# Pebble Beach 2019 and Erin Hills 2017 are Tier 4.
tier12 = np.array([d[2] for d in MODERN_DATA if d[5] in ("T1", "T2")])
tier34 = np.array([d[2] for d in MODERN_DATA if d[5] in ("T3", "T4")])

# ==============================================================================
# SECTION 2: DESCRIPTIVE STATISTICS
# Corresponds to Table 1 in manuscript
# ==============================================================================

print("=" * 70)
print("TABLE 1: DESCRIPTIVE STATISTICS — Modern Era FSA+/- (2010-2025)")
print("=" * 70)
print(f"  n                      : {len(fsa_values)}")
print(f"  Mean (μ)               : {np.mean(fsa_values):.3f}")
print(f"  Standard deviation (SD): {np.std(fsa_values, ddof=1):.3f}")
print(f"  Standard error (SE)    : {np.std(fsa_values, ddof=1) / np.sqrt(len(fsa_values)):.3f}")
print(f"  Median                 : {np.median(fsa_values):.3f}")
print(f"  Minimum                : {np.min(fsa_values):.2f}  ({[d[1] for d in MODERN_DATA if d[2]==np.min(fsa_values)][0]}, {[d[0] for d in MODERN_DATA if d[2]==np.min(fsa_values)][0]})")
print(f"  Maximum                : {np.max(fsa_values):.2f}  ({[d[1] for d in MODERN_DATA if d[2]==np.max(fsa_values)][0]}, {[d[0] for d in MODERN_DATA if d[2]==np.max(fsa_values)][0]})")
print(f"  Range                  : {np.max(fsa_values) - np.min(fsa_values):.3f}")
print()
print("  Tier composition:")
print(f"    Tier 1 (Unconditional):       n={len(tier1)}, values={sorted(tier1.tolist(), reverse=True)}")
print(f"    Tier 2 (Strong case):         n={len(tier2)}, values={sorted(tier2.tolist(), reverse=True)}")
print(f"    Tier 3 (Condition-sensitive): n={len(tier3)}, values={sorted(tier3.tolist(), reverse=True)}")
print(f"    Tier 4 (Easy):                n={len(tier4)}, values={sorted(tier4.tolist(), reverse=True)}")

# ==============================================================================
# TEST 1: WELCH'S TWO-SAMPLE T-TEST — Tier 1+2 vs Tier 3+4
# Corresponds to Table 3 in manuscript
# H0: mean(Tier1+2) = mean(Tier3+4)
# H1: mean(Tier1+2) > mean(Tier3+4)  [one-tailed]
# Using two-tailed test as reported in manuscript; p/2 for one-tailed
# ==============================================================================

print()
print("=" * 70)
print("TABLE 3: WELCH'S TWO-SAMPLE T-TEST — Tier 1+2 vs Tier 3+4")
print("H0: mu(Tier1+2) = mu(Tier3+4)")
print("H1: mu(Tier1+2) != mu(Tier3+4)  [two-tailed as reported in manuscript]")
print("=" * 70)

t_stat, p_two = stats.ttest_ind(tier12, tier34, equal_var=False)

# Cohen's d (pooled SD estimate)
pooled_sd = np.sqrt((np.std(tier12, ddof=1)**2 + np.std(tier34, ddof=1)**2) / 2)
cohens_d  = (np.mean(tier12) - np.mean(tier34)) / pooled_sd

# Welch-Satterthwaite degrees of freedom
s1, s2 = np.std(tier12, ddof=1), np.std(tier34, ddof=1)
n1, n2 = len(tier12), len(tier34)
df_ws = (s1**2/n1 + s2**2/n2)**2 / ((s1**2/n1)**2/(n1-1) + (s2**2/n2)**2/(n2-1))

print(f"  Tier 1+2: n={n1}, mean={np.mean(tier12):.3f}, SD={s1:.3f}")
print(f"  Tier 3+4: n={n2}, mean={np.mean(tier34):.3f}, SD={s2:.3f}")
print(f"  Mean difference: {np.mean(tier12) - np.mean(tier34):.3f}")
print(f"  Welch's t statistic: {t_stat:.3f}")
print(f"  Degrees of freedom (Welch-Satterthwaite): {df_ws:.1f}")
print(f"  p-value (two-tailed): {p_two:.4f}")
print(f"  p-value (one-tailed, Tier1+2 > Tier3+4): {p_two/2:.4f}")
print(f"  Cohen's d (effect size): {cohens_d:.3f}")
print(f"  Interpretation: {'Very large' if cohens_d > 1.2 else 'Large' if cohens_d > 0.8 else 'Medium' if cohens_d > 0.5 else 'Small'} effect (Cohen, 1988)")
print(f"  Decision (alpha=0.05, two-tailed): {'REJECT H0 — SIGNIFICANT' if p_two < 0.05 else 'FAIL TO REJECT H0'}")

# ==============================================================================
# TEST 2: ONE-WAY ANOVA — FSA+/- across all four tiers
# Corresponds to Table 4 in manuscript
# H0: mu1 = mu2 = mu3 = mu4
# H1: at least one tier mean differs
# ==============================================================================

print()
print("=" * 70)
print("TABLE 4: ONE-WAY ANOVA — FSA+/- Across Four Venue Tiers")
print("H0: mu(T1) = mu(T2) = mu(T3) = mu(T4)")
print("H1: at least one tier mean differs")
print("=" * 70)

f_stat, p_anova = stats.f_oneway(tier1, tier2, tier3, tier4)

# Eta-squared: SSbetween / SStotal
all_groups = np.concatenate([tier1, tier2, tier3, tier4])
grand_mean = np.mean(all_groups)
ss_between = (len(tier1)*(np.mean(tier1)-grand_mean)**2 +
              len(tier2)*(np.mean(tier2)-grand_mean)**2 +
              len(tier3)*(np.mean(tier3)-grand_mean)**2 +
              len(tier4)*(np.mean(tier4)-grand_mean)**2)
ss_total   = np.sum((all_groups - grand_mean)**2)
eta_sq     = ss_between / ss_total

df_between = 3
df_within  = len(all_groups) - 4

print(f"  Tier 1: n={len(tier1)}, mean={np.mean(tier1):.3f}, SD={np.std(tier1,ddof=1):.3f}")
print(f"  Tier 2: n={len(tier2)}, mean={np.mean(tier2):.3f}, SD={np.std(tier2,ddof=1):.3f}")
print(f"  Tier 3: n={len(tier3)}, mean={np.mean(tier3):.3f}, SD={np.std(tier3,ddof=1):.3f}")
print(f"  Tier 4: n={len(tier4)}, mean={np.mean(tier4):.3f}, SD={np.std(tier4,ddof=1):.3f}")
print(f"  Grand mean: {grand_mean:.3f}")
print(f"  F statistic: {f_stat:.3f}")
print(f"  df between groups: {df_between}")
print(f"  df within groups: {df_within}")
print(f"  p-value: {p_anova:.4f}")
print(f"  Eta-squared (eta^2): {eta_sq:.3f}  ({eta_sq*100:.1f}% variance explained by tier)")
print(f"  Decision (alpha=0.05): {'REJECT H0 — SIGNIFICANT' if p_anova < 0.05 else 'FAIL TO REJECT H0'}")

# ==============================================================================
# TEST 3: WELCH'S T-TEST — 2021 U.S. Amateur Natural Experiment
# Corresponds to Table 5 in manuscript
# Oakmont CC vs Longue Vue Club, Round 1 stroke play
# H0: mu(Oakmont) = mu(Longue Vue)
# H1: mu(Oakmont) > mu(Longue Vue)  [one-tailed; Oakmont hypothesized harder]
# ==============================================================================

print()
print("=" * 70)
print("TABLE 5: 2021 U.S. AMATEUR NATURAL EXPERIMENT")
print("Oakmont CC vs Longue Vue Club — Round 1 Stroke Play")
print("H0: mu(Oakmont) = mu(Longue Vue)")
print("H1: mu(Oakmont) > mu(Longue Vue)  [one-tailed]")
print("=" * 70)
print()
print("  DATA SOURCE: USGA Media Center 2021 U.S. Amateur press releases")
print("  Round 1 averages reported by USGA for each co-host venue.")
print("  Individual player scores not publicly available; test uses")
print("  summary statistics with estimated per-player SDs.")
print()

# Published round averages (USGA Media Center)
mean_oak = 77.16   # Oakmont CC, par 70
mean_lv  = 71.69   # Longue Vue Club, par 70
n_oak    = 78      # approximate (312 players / 2 venues / 2 rounds)
n_lv     = 78

# Per-player SD estimated from USGA Amateur stroke play dispersion
# (Broadie 2012; typical elite amateur SP events show SD 3-4 strokes)
# Oakmont slightly higher variance given extreme difficulty
sd_oak   = 3.8    # estimated
sd_lv    = 3.1    # estimated

# Welch's t-test using summary statistics
se_diff  = np.sqrt(sd_oak**2/n_oak + sd_lv**2/n_lv)
t_am     = (mean_oak - mean_lv) / se_diff

# Welch-Satterthwaite df
df_am = (sd_oak**2/n_oak + sd_lv**2/n_lv)**2 / (
    (sd_oak**2/n_oak)**2/(n_oak-1) + (sd_lv**2/n_lv)**2/(n_lv-1))

# One-tailed p-value
p_am_one = stats.t.sf(t_am, df=df_am)

# Cohen's d
pooled_sd_am = np.sqrt((sd_oak**2 + sd_lv**2) / 2)
d_am         = (mean_oak - mean_lv) / pooled_sd_am

print(f"  Oakmont CC:     n≈{n_oak}, mean={mean_oak}, SD≈{sd_oak} (estimated)")
print(f"  Longue Vue:     n≈{n_lv},  mean={mean_lv},  SD≈{sd_lv} (estimated)")
print(f"  Mean difference: {mean_oak - mean_lv:.2f} strokes (Oakmont harder)")
print(f"  SE of difference: {se_diff:.4f}")
print(f"  Welch's t statistic: {t_am:.3f}")
print(f"  df (Welch-Satterthwaite): {df_am:.0f}")
print(f"  p-value (one-tailed): {p_am_one:.8f}")
print(f"  Cohen's d: {d_am:.3f}")
print(f"  Decision (alpha=0.001): {'REJECT H0 — HIGHLY SIGNIFICANT' if p_am_one < 0.001 else 'NOT SIGNIFICANT at 0.001'}")
print()
print("  ROBUSTNESS CHECK: Result is robust to SD assumptions.")
print("  With SD=5.0 at both venues (conservative overestimate):")
se_conservative = np.sqrt(5.0**2/n_oak + 5.0**2/n_lv)
t_conservative  = (mean_oak - mean_lv) / se_conservative
df_conservative = (5.0**2/n_oak + 5.0**2/n_lv)**2 / (
    (5.0**2/n_oak)**2/(n_oak-1) + (5.0**2/n_lv)**2/(n_lv-1))
p_conservative  = stats.t.sf(t_conservative, df=df_conservative)
print(f"    t = {t_conservative:.3f}, df ≈ {df_conservative:.0f}, p = {p_conservative:.8f}")
print(f"    Conclusion: Still highly significant at p < 0.0001")

