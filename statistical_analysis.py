# ==============================================================================
# SECTION 3: FIELD-STRENGTH REGRESSION (Table 12)
# Tests whether year-to-year field quality confounds FSA+/- comparisons
# Data source: us_open_strengths.csv (Data Golf pre-cut Round 1 field strength)
# ==============================================================================

print()
print("=" * 70)
print("TABLE 12: FIELD-STRENGTH-ADJUSTED FSA+/- (REGRESSION RESIDUALS)")
print("Tests §6.3.2: is FSA+/- confounded by year-to-year field quality?")
print("=" * 70)

import csv

# Load field strength data (Round 1, pre-cut, full field)
field_strength_path = "us_open_strengths.csv"
try:
    with open(field_strength_path) as f:
        reader = csv.DictReader(f)
        fs_rows = [r for r in reader if r["round_num"] == "1"]
except FileNotFoundError:
    print(f"  [us_open_strengths.csv not found in working directory — skipping Table 12]")
    fs_rows = []

if fs_rows:
    # Map (year, tournament_num) -> field_strength, correctly disambiguating
    # the two 2021-labeled PGA Tour season events:
    #   tournament_num 26,  date 2020-09-20 -> actually the 2020 U.S. Open (Winged Foot)
    #   tournament_num 535, date 2021-06-20 -> actually the 2021 U.S. Open (Torrey Pines)
    venue_label_map = {
        ("2010", "26"):  "Pebble Beach 2010",
        ("2011", "26"):  "Congressional 2011",
        ("2012", "26"):  "Olympic Club 2012",
        ("2013", "26"):  "Merion 2013",
        ("2014", "26"):  "Pinehurst 2014",
        ("2015", "26"):  "Chambers Bay 2015",
        ("2016", "26"):  "Oakmont 2016",
        ("2017", "26"):  "Erin Hills 2017",
        ("2018", "26"):  "Shinnecock 2018",
        ("2019", "26"):  "Pebble Beach 2019",
        ("2021", "26"):  "Winged Foot 2020",   # played Sept 2020; counted in 2021 PGA Tour season
        ("2021", "535"): "Torrey Pines 2021",  # actual June 2021 U.S. Open
        ("2022", "26"):  "Country Club 2022",
        ("2023", "26"):  "LACC 2023",
        ("2024", "26"):  "Pinehurst 2024",
        ("2025", "26"):  "Oakmont 2025",
        ("2026", "26"):  "Shinnecock 2026",
    }

    field_strength = {}
    for r in fs_rows:
        key = (r["tournament_year"], r["tournament_num"])
        label = venue_label_map.get(key)
        if label:
            field_strength[label] = float(r["field_strength"])

    # FSA+/- values (n=17), matching Table 2 in the manuscript
    fsa = {
        "Merion 2013": 4.55, "Shinnecock 2018": 4.39, "Winged Foot 2020": 4.07,
        "Pebble Beach 2010": 3.98, "Oakmont 2025": 3.82, "Olympic Club 2012": 3.47,
        "Chambers Bay 2015": 3.45, "Oakmont 2016": 3.33, "Pinehurst 2014": 3.09,
        "Pinehurst 2024": 2.78, "Shinnecock 2026": 2.68, "Country Club 2022": 2.60,
        "Torrey Pines 2021": 2.27, "LACC 2023": 1.76, "Congressional 2011": 1.64,
        "Pebble Beach 2019": 1.22, "Erin Hills 2017": 1.14,
    }

    common = sorted(set(field_strength.keys()) & set(fsa.keys()))
    x = np.array([field_strength[k] for k in common])
    y = np.array([fsa[k] for k in common])

    r_corr, p_corr = stats.pearsonr(x, y)
    slope, intercept, r_val, p_val, std_err = stats.linregress(x, y)
    predicted = intercept + slope * x
    residuals = y - predicted

    print(f"  n = {len(common)} venues matched")
    print(f"  Pearson correlation: r = {r_corr:.3f}, p = {p_corr:.4f}")
    print(f"  Linear regression: FSA+/- = {intercept:.3f} + {slope:.3f} * field_strength")
    print(f"  R² = {r_val**2:.4f} (field strength explains {r_val**2*100:.1f}% of variance)")
    print(f"  slope SE = {std_err:.4f}, p = {p_val:.4f}")
    print(f"  Result: {'SIGNIFICANT' if p_val < 0.05 else 'NOT SIGNIFICANT'} at alpha=0.05")
    print(f"  Interpretation: {'Field strength IS a significant confound' if p_val < 0.05 else 'Field strength is NOT a significant confound — supports inflation-adjustment assumption'}")
    print()
    print(f"  {'Venue':22s} {'FieldStr':>10s} {'FSA+/-':>8s} {'Predicted':>10s} {'Residual':>10s}")
    for k in sorted(common, key=lambda v: -(fsa[v] - (intercept + slope*field_strength[v]))):
        resid = fsa[k] - (intercept + slope*field_strength[k])
        print(f"  {k:22s} {field_strength[k]:10.4f} {fsa[k]:8.2f} {intercept+slope*field_strength[k]:10.3f} {resid:+10.3f}")

print()
